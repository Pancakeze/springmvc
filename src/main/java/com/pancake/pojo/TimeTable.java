package com.pancake.pojo;

import java.util.Date;

public class TimeTable {
    private Integer id;

    private Date first;

    private Date second;

    private Date third;

    private Date fourth;

    private Date fifth;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Date getFirst() {
        return first;
    }

    public void setFirst(Date first) {
        this.first = first;
    }

    public Date getSecond() {
        return second;
    }

    public void setSecond(Date second) {
        this.second = second;
    }

    public Date getThird() {
        return third;
    }

    public void setThird(Date third) {
        this.third = third;
    }

    public Date getFourth() {
        return fourth;
    }

    public void setFourth(Date fourth) {
        this.fourth = fourth;
    }

    public Date getFifth() {
        return fifth;
    }

    public void setFifth(Date fifth) {
        this.fifth = fifth;
    }
}